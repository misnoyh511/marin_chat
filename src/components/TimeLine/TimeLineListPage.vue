<template>
  <v-ons-page>
    <v-ons-toolbar>
      <div class="left">
        <div class="toolbar-heading">タイムライン</div>
      </div>
    </v-ons-toolbar>
    <v-ons-pull-hook :action="loadTimeLineItem" @changestate="state = $event.state">
      <span v-show="state === 'initial'">更新</span>
      <span v-show="state === 'action'">読み込み中</span>
    </v-ons-pull-hook>
    <v-ons-list>
      <div v-for="item in $root.timeLineItems" :key="item.id">
        <v-ons-list-item modifier="nodivider">
          <div class="left">
            <img class="list-item__thumbnail" :src="`http://a1333.ml/img/profile/${item.uuid}.jpg`" @click="showProfileDetailModalWindow(item.id)">
          </div>
          <div class="center" @click="showUserTimeLine(item.user_id)">
            <span class="list-item__title">{{ item.name }}</span>
            <span class="list-item__title color-gray">{{ `${getGenderString(item.gender)} / ${item.age}歳 / ${item.prefecture}` }}</span>
          </div>
          <div class="right">
            <span>{{ [item.elapsed_seconds, 'seconds'] | duration('humanize', true) }}</span>
            <v-ons-icon v-if="item.user_id === $root.userId" icon="fa-trash" class="pl10" @click="deletePostFromTimeLine(item.id)" />
            <!--<v-ons-icon v-if="item.user_id !== $root.userId" icon="fa-heart" class="color-pink pl10" @click="sendHeart(item.user_id)" />-->
          </div>
        </v-ons-list-item>
        <v-ons-list-item  class="no-bg pa-0">
          <div class="center" @click="showUserTimeLine(item.user_id)">
            {{item.msg}}
          </div>
        </v-ons-list-item>
        <v-ons-list-item modifier="longdivider" class="longdivider gray-btns">
          <div class="left">
            <ons-button  @click="sendHeart(item.user_id)" > <v-ons-icon v-if="item.user_id !== $root.userId" icon="fa-heart" class="color-pink pl10" /> いいね</ons-button>
          </div>
          <div class="center">
            <ons-button  v-if="$root.userId != item.user_id" @click="showMessagePage(item.user_id)"><v-ons-icon icon="fa-envelope"></v-ons-icon> トーク</ons-button>
          </div>
          <div class="right">
            <ons-button class="triangle-btn" v-if="item.user_id !== $root.userId" @click="sendReport(item.user_id, item.id)">
              <v-ons-icon icon="fa-exclamation-triangle" class="color-gray"/> 通報
            </ons-button>

          </div>
        </v-ons-list-item>

      </div>
    </v-ons-list>

    <v-ons-modal v-if="profileDetailModalWindowVisible === true" :visible="profileDetailModalWindowVisible" @click="profileDetailModalWindowVisible = false" class="profile-modal">
      <h1>{{ modalItem.name }}</h1>
      <div class="user-info">
        <span>{{ `${getGenderString(modalItem.gender)} / ${modalItem.age}歳 / ${modalItem.prefecture}` }}</span>
      </div>
      <img class="fullsize-profile-image" :src="`http://a1333.ml/img/profile/${modalItem.uuid}.jpg`">
      <div class="user-info">
        <span>
          {{ modalItem.comment }}
        </span>
      </div>
      <ons-button v-if="$root.userId != modalItem.user_id" @click="showMessagePage(modalItem.user_id)"> <v-ons-icon icon="fa-envelope" /> メッセージを送る</ons-button>
    </v-ons-modal>

    <v-ons-fab ripple position="bottom right" class="bottom-pencil">
      <v-ons-icon icon="fa-pencil" @click="showPostPage" />
    </v-ons-fab>
  </v-ons-page>
</template>

<script>
import TimeLinePostPage from './TimeLinePostPage'
import MessagePage from '../Message/MessagePage'
import UserTimeLineListPage from './UserTimeLineListPage'
import Enumerable from 'linq'

export default {
  data () {
    return {
      state: 'initial',
      profileDetailModalWindowVisible: false,
      modalItem: null
    }
  },
  async mounted () {
    await this.$root.getTimeLineItems()
  },
  methods: {
    async loadTimeLineItem (done) {
      await this.$root.getTimeLineItems()
      done()
    },
    showPostPage: function () {
      if (this.$root.userId === null) {
        alert(this.$root.needRegisterMessage)
        this.$root.activeTabIndex = this.$root.UserProfileSettingTabIndex
      } else {
        this.$root.timeLinePageStack.push(TimeLinePostPage)
      }
    },
    showMessagePage: function (userId) {
      if (this.$root.userId === null) {
        alert(this.$root.needRegisterMessage)
        this.$root.activeTabIndex = this.$root.UserProfileSettingTabIndex
      } else {
        this.$root.activeTabIndex = this.$root.messageTabIndex
        while (this.$root.messagePageStack.length > 1) {
          this.$root.messagePageStack.pop()
        }
        this.$root.messagePageStack.push({
          extends: MessagePage,
          data () {
            return {
              toUserId: userId
            }
          }
        })
      }
    },
    showUserTimeLine (userId) {
      this.$root.timeLinePageStack.push({
        extends: UserTimeLineListPage,
        data () {
          return {
            userId: userId
          }
        }
      })
    },
    getGenderString: function (gender) {
      let genders = [ '男性', '女性', '非公開' ]
      return genders[gender]
    },
    showProfileDetailModalWindow: function (id) {
      let item = Enumerable.from(this.$root.timeLineItems)
        .where(x => x.id === id)
        .first()
      this.modalItem = item
      this.profileDetailModalWindowVisible = true
    },
    async deletePostFromTimeLine (id) {
      let result = confirm('削除しますか？')
      if (result === true) {
        let response = await fetch('http://a1333.ml/api/delete_post_from_timeline', {
          method: 'post',
          headers: { 'Content-type': 'application/json; charset=UTF-8' },
          mode: 'cors',
          credentials: 'include',
          body: JSON.stringify(
            {
              id: id
            })
        })
        let json = await response.json()
        if (json.success === true) {
          await this.$root.getTimeLineItems()
          alert('削除しました。')
        }
      }
    },
    async sendHeart (userId) {
      if (this.$root.userId === null) {
        alert(this.$root.needRegisterMessage)
        this.$root.activeTabIndex = this.$root.UserProfileSettingTabIndex
      } else {
        await this.$root.postMessage(userId, 'いいね！')
        alert('「いいね」しました。')
      }
    },
    async sendReport (userId, messageId) {
      if (this.$root.userId === null) {
        alert(this.$root.needRegisterMessage)
        this.$root.activeTabIndex = this.$root.UserProfileSettingTabIndex
      } else {
        let result = confirm('通報しますか？')
        if (result === true) {
          await this.$root.sendReport(userId, messageId)
          alert('通報しました。')
        }
      }
    }
  }
}
</script>

